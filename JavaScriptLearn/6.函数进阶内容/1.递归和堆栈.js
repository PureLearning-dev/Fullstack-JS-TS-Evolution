// 递归是一种编程模式，在一个任务可以自然地拆分成多个相同类型但更简单的任务的情况下非常有用。或者，在一个任务可以简化为一个简单的行为加上该任务的一个更简单的变体的时候可以使用。
// 当一个函数解决一个任务时，在解决的过程中它可以调用很多其它函数。在部分情况下，函数会调用 自身。这就是所谓的 递归。
// 有两种典型的思考方法——迭代和递归，迭代使用 for 循环思想，递归使用的是分解的思想。

function pow(a, b) {
    let i = 0;
    let num = 1;
    while (i < b) {
        num *= a;
        i++;
    }
    return num;
}

console.log(pow(2, 3));

function pow1(a, b) {
    if(b === 0) return 1;
    return a * pow1(a, b - 1);
}

console.log(pow1(2, 3));

// 递归解通常比迭代解更短，但执行的次数一般更多，除非进行一些优化。
// 递归使用的是栈，其最深深度是由 JS 引擎决定的——称为递归深度。

// 有关正在运行的函数的执行过程的相关信息被存储在其 执行上下文 中。
//
// 执行上下文 是一个内部数据结构，它包含有关函数执行时的详细细节：当前控制流所在的位置，当前的变量，this 的值（此处我们不使用它），以及其它的一些内部细节。

// 一个函数调用仅具有一个与其相关联的执行上下文。
//
// 当一个函数进行嵌套调用时，将发生以下的事儿：
//
// 当前函数被暂停；
// 与它关联的执行上下文被一个叫做 执行上下文堆栈 的特殊数据结构保存；
// 执行嵌套调用；
// 嵌套调用结束后，从堆栈中恢复之前的执行上下文，并从停止的位置恢复外部函数。

// 任何递归都可以用循环来重写。通常循环变体更有效。

// 递归的一个重要应用就是递归遍历。

let company = {
    sales: [{
        name: 'John',
        salary: 1000
    }, {
        name: 'Alice',
        salary: 1600
    }],

    development: {
        sites: [{
            name: 'Peter',
            salary: 2000
        }, {
            name: 'Alex',
            salary: 1800
        }],

        internals: [{
            name: 'Jack',
            salary: 1300
        }]
    }
};
// 我们要获取上述公司所有员工的工资之和！
function sumSalaries(obj) {
    // 如果是数组 → 把每个元素的 salary 加上，嵌套对象继续递归
    if (Array.isArray(obj)) {
        return obj.reduce((sum, item) => {
            if (item.salary) {
                sum += item.salary;
            }
            return sum + sumSalaries(item); // 以防数组元素还有嵌套
        }, 0);
    }

    // 如果是对象 → 遍历每个属性，递归处理
    if (typeof obj === 'object' && obj !== null) {
        let sum = 0;
        for (let key in obj) {
            sum += sumSalaries(obj[key]);
        }
        return sum;
    }

    return 0; // 既不是数组也不是对象（基本类型），没有 salary
}

console.log(sumSalaries(company));

